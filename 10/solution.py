def solve(puzzle_input):
    lines = puzzle_input.strip().split("\n")

    corrupt_score = 0
    incomplete_scores = []
    for line in lines:
        chunk_stack = []
        corrupt = False
        for char in line:
            if char == "[" or char == "(" or char == "{" or char == "<":
                # new chunk
                chunk_stack.append(char)
            else:
                last_chunk = chunk_stack[-1]
                if (last_chunk == "[" and char != "]") or (last_chunk == "(" and char != ")") or (last_chunk == "{" and char != "}") or (last_chunk == "<" and char != ">"):
                    # corrupt
                    corrupt = True
                    if char == ")":
                        corrupt_score += 3
                    elif char == "]":
                        corrupt_score += 57
                    elif char == "}":
                        corrupt_score += 1197
                    elif char == ">":
                        corrupt_score += 25137
                    break
                else:
                    chunk_stack.pop()
        if not corrupt:
            if len(chunk_stack) > 0:
                # incomplete
                incomplete_score = 0
                while len(chunk_stack) > 0:
                    incomplete_score *= 5
                    last_chunk = chunk_stack.pop()
                    if last_chunk == "(":
                        incomplete_score += 1
                    elif last_chunk == "[":
                        incomplete_score += 2
                    elif last_chunk == "{":
                        incomplete_score += 3
                    elif last_chunk == "<":
                        incomplete_score += 4
                incomplete_scores.append(incomplete_score)
    incomplete_scores.sort()

    # part 1
    print(corrupt_score)

    # part 2
    print(incomplete_scores[int(len(incomplete_scores) / 2)])

    return


